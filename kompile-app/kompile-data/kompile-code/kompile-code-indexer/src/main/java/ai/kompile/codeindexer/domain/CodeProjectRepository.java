package ai.kompile.codeindexer.domain;

import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.stereotype.Repository;

import java.util.List;
import java.util.Optional;
import java.util.UUID;

@Repository
public interface CodeProjectRepository extends JpaRepository<CodeProject, UUID> {

    Optional<CodeProject> findByProjectId(String projectId);

    List<CodeProject> findByIsActiveTrue();

    Optional<CodeProject> findByFactSheetId(Long factSheetId);

    boolean existsByProjectId(String projectId);
}
