    @CommandLine.Command(name = "bulk-accept", description = "Bulk accept multiple proposals",
            mixinStandardHelpOptions = true)
    static class BulkAcceptCmd implements Callable<Integer> {
        @CommandLine.Mixin private AppClientMixin app;

        @CommandLine.Option(names = "--proposal-ids", split = ",", required = true,
                description = "Comma-separated proposal IDs to accept")
        private List<String> proposalIds;

        @CommandLine.Option(names = "--reviewed-by", defaultValue = "cli-user",
                description = "Reviewer name")
        private String reviewedBy;

        @CommandLine.Option(names = "--storage-type",
                description = "Storage type (e.g., JPA, NEO4J)")
        private String storageType;

        @Override
        public Integer call() {
            KompileHttpClient client = app.requireClient();
            if (client == null) return 1;
            try {
                Map<String, Object> body = new LinkedHashMap<>();
                body.put("proposalIds", proposalIds);
                body.put("reviewedBy", reviewedBy);
                if (storageType != null) body.put("storageType", storageType);

                String response = client.postString(
                        "/api/knowledge-graph/builder/proposals/bulk-accept", body);
                if (app.isJsonOutput()) {
                    OutputFormatter.printJson(response);
                } else {
                    JsonNode result = client.getObjectMapper().readTree(response);
                    System.out.println("Bulk accept complete.");
                    result.fields().forEachRemaining(e -> OutputFormatter.printKv(e.getKey(), e.getValue()));
                }
                return 0;
            } catch (Exception e) {
                System.err.println("Error: " + e.getMessage());
                return 1;
            }
        }
    }

