    // proposals reject <proposalId>
    // ═══════════════════════════════════════════════════════════════════════════

    @CommandLine.Command(name = "reject", description = "Reject a triple proposal",
            mixinStandardHelpOptions = true)
    static class RejectCmd implements Callable<Integer> {
        @CommandLine.Mixin private AppClientMixin app;

        @CommandLine.Parameters(index = "0", description = "Proposal ID to reject")
        private String proposalId;

        @CommandLine.Option(names = "--reason", description = "Rejection reason")
        private String reason;

        @CommandLine.Option(names = "--reviewed-by", defaultValue = "cli-user",
                description = "Reviewer name")
        private String reviewedBy;

        @Override
        public Integer call() {
            KompileHttpClient client = app.requireClient();
            if (client == null) return 1;
            try {
                Map<String, Object> body = new LinkedHashMap<>();
                if (reason != null) body.put("reason", reason);
                body.put("reviewedBy", reviewedBy);

                String response = client.postString(
                        "/api/knowledge-graph/builder/proposals/" + proposalId + "/reject", body);
                if (app.isJsonOutput()) {
                    OutputFormatter.printJson(response);
                } else {
                    System.out.println("Rejected proposal: " + proposalId);
                }
                return 0;
            } catch (Exception e) {
                System.err.println("Error: " + e.getMessage());
                return 1;
            }
        }
    }

    // ═══════════════════════════════════════════════════════════════════════════
