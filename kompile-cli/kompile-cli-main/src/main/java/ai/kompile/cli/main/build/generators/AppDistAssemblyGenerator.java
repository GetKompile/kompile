package ai.kompile.cli.main.build.generators;

import ai.kompile.cli.main.build.config.BuildConfiguration;

import java.io.*;

/**
 * Generates distribution packaging artifacts for a generated kompile application:
 * <ul>
 *   <li>{@code src/main/assembly/app-dist.xml} &mdash; Maven Assembly descriptor that bundles
 *       the native binary (or fat JAR), configuration, data scaffolding, and launcher scripts
 *       into a zip/tar.gz.</li>
 *   <li>{@code src/main/scripts/run.sh} &mdash; Orchestrator launcher that starts the app
 *       (prefers native binary, falls back to JAR).</li>
 * </ul>
 *
 * <p>The generated assembly is activated by a {@code dist} Maven profile added to the POM
 * by {@link PomModelBuilder}.</p>
 */
public class AppDistAssemblyGenerator {

    private final BuildConfiguration config;

    public AppDistAssemblyGenerator(BuildConfiguration config) {
        this.config = config;
    }

    /**
     * Generate all distribution artifacts under the given project directory.
     */
    public void generate(File projectDir) throws IOException {
        generateAssemblyDescriptor(projectDir);
        generateLauncherScript(projectDir);
        generateDataScaffolding(projectDir);
        // Ensure companion-binaries directory exists (assembly references it)
        File companionDir = new File(projectDir, "companion-binaries");
        if (!companionDir.exists()) {
            companionDir.mkdirs();
        }
        System.out.println("Generated distribution packaging in: " + projectDir.getAbsolutePath());
    }

    private void generateAssemblyDescriptor(File projectDir) throws IOException {
        File assemblyDir = new File(projectDir, "src/main/assembly");
        if (!assemblyDir.exists() && !assemblyDir.mkdirs()) {
            throw new IOException("Could not create assembly directory: " + assemblyDir.getAbsolutePath());
        }

        String appName = config.getConfigName();
        String nativeImageName = appName + "-native";

        File assemblyFile = new File(assemblyDir, "app-dist.xml");
        try (PrintWriter w = new PrintWriter(new FileWriter(assemblyFile))) {
            w.println("<!--");
            w.println("  Generated by kompile build app");
            w.println("  Distribution assembly for: " + appName);
            w.println("-->");
            w.println("<assembly xmlns=\"http://maven.apache.org/ASSEMBLY/2.2.0\"");
            w.println("          xmlns:xsi=\"http://www.w3.org/2001/XMLSchema-instance\"");
            w.println("          xsi:schemaLocation=\"http://maven.apache.org/ASSEMBLY/2.2.0");
            w.println("                              http://maven.apache.org/xsd/assembly-2.2.0.xsd\">");
            w.println("    <id>dist</id>");
            w.println("    <formats>");
            w.println("        <format>zip</format>");
            w.println("        <format>tar.gz</format>");
            w.println("    </formats>");
            w.println("    <includeBaseDirectory>true</includeBaseDirectory>");
            w.println("    <baseDirectory>" + appName + "-${project.version}</baseDirectory>");
            w.println();

            // --- FileSets section ---
            w.println("    <fileSets>");

            // Native binary
            w.println("        <!-- Native binary -->");
            w.println("        <fileSet>");
            w.println("            <directory>${project.build.directory}</directory>");
            w.println("            <outputDirectory>bin</outputDirectory>");
            w.println("            <includes>");
            w.println("                <include>" + nativeImageName + "</include>");
            w.println("            </includes>");
            w.println("            <fileMode>0755</fileMode>");
            w.println("        </fileSet>");

            // Companion native binaries (model-staging, CLI)
            w.println("        <!-- Companion native binaries (model-staging, CLI) -->");
            w.println("        <fileSet>");
            w.println("            <directory>${project.basedir}/companion-binaries</directory>");
            w.println("            <outputDirectory>bin</outputDirectory>");
            w.println("            <includes>");
            w.println("                <include>*</include>");
            w.println("            </includes>");
            w.println("            <fileMode>0755</fileMode>");
            w.println("        </fileSet>");

            // GraalVM shared libs emitted next to the native binary
            w.println("        <!-- GraalVM shared libraries (emitted next to native binary) -->");
            w.println("        <fileSet>");
            w.println("            <directory>${project.build.directory}</directory>");
            w.println("            <outputDirectory>bin</outputDirectory>");
            w.println("            <includes>");
            w.println("                <include>lib*.so</include>");
            w.println("                <include>lib*.dylib</include>");
            w.println("                <include>lib*.dll</include>");
            w.println("            </includes>");
            w.println("            <fileMode>0755</fileMode>");
            w.println("        </fileSet>");

            // Native libraries from platform JARs (ND4J, bytedeco, JavaCPP)
            w.println("        <!-- Native libraries (ND4J, bytedeco, JavaCPP platform .so/.dylib/.dll) -->");
            w.println("        <fileSet>");
            w.println("            <directory>${project.build.directory}/native-libs</directory>");
            w.println("            <outputDirectory>lib</outputDirectory>");
            w.println("            <includes>");
            w.println("                <include>**/*.so</include>");
            w.println("                <include>**/*.so.*</include>");
            w.println("                <include>**/*.dylib</include>");
            w.println("                <include>**/*.dll</include>");
            w.println("            </includes>");
            w.println("            <fileMode>0755</fileMode>");
            w.println("        </fileSet>");

            // Launcher scripts
            w.println("        <!-- Launcher scripts -->");
            w.println("        <fileSet>");
            w.println("            <directory>${project.basedir}/src/main/scripts</directory>");
            w.println("            <outputDirectory>bin</outputDirectory>");
            w.println("            <includes>");
            w.println("                <include>*.sh</include>");
            w.println("            </includes>");
            w.println("            <fileMode>0755</fileMode>");
            w.println("        </fileSet>");

            // Configuration
            w.println("        <!-- Application configuration -->");
            w.println("        <fileSet>");
            w.println("            <directory>${project.basedir}/src/main/resources</directory>");
            w.println("            <outputDirectory>conf</outputDirectory>");
            w.println("            <includes>");
            w.println("                <include>application.properties</include>");
            w.println("                <include>application-*.properties</include>");
            w.println("                <include>log4j2*.xml</include>");
            w.println("                <include>log4j2.component.properties</include>");
            w.println("            </includes>");
            w.println("        </fileSet>");

            // Data scaffolding
            w.println("        <!-- Data directory scaffolding -->");
            w.println("        <fileSet>");
            w.println("            <directory>${project.basedir}/data</directory>");
            w.println("            <outputDirectory>data</outputDirectory>");
            w.println("            <includes>");
            w.println("                <include>input_documents/**</include>");
            w.println("                <include>shared_files/**</include>");
            w.println("                <include>prompt-templates/**</include>");
            w.println("                <include>mcp-config.json</include>");
            w.println("            </includes>");
            w.println("        </fileSet>");

            // Empty runtime directories
            w.println("        <!-- Empty runtime directories -->");
            for (String dir : new String[]{"logs", "models"}) {
                w.println("        <fileSet>");
                w.println("            <directory>${project.basedir}/src/main/assembly</directory>");
                w.println("            <outputDirectory>" + dir + "</outputDirectory>");
                w.println("            <excludes>");
                w.println("                <exclude>**/*</exclude>");
                w.println("            </excludes>");
                w.println("        </fileSet>");
            }

            w.println("    </fileSets>");
            w.println("</assembly>");
        }
    }

    private void generateLauncherScript(File projectDir) throws IOException {
        File scriptsDir = new File(projectDir, "src/main/scripts");
        if (!scriptsDir.exists() && !scriptsDir.mkdirs()) {
            throw new IOException("Could not create scripts directory: " + scriptsDir.getAbsolutePath());
        }

        String appName = config.getConfigName();
        String nativeImageName = appName + "-native";

        File runScript = new File(scriptsDir, "run.sh");
        try (PrintWriter w = new PrintWriter(new FileWriter(runScript))) {
            w.println("#!/usr/bin/env bash");
            w.println("#");
            w.println("# Launcher for " + appName);
            w.println("# Generated by kompile build app");
            w.println("#");
            w.println("# Usage:");
            w.println("#   bin/run.sh");
            w.println("#   bin/run.sh --server.port=9090");
            w.println("#");
            w.println();
            w.println("set -e");
            w.println();
            w.println("SOURCE=\"${BASH_SOURCE[0]}\"");
            w.println("SCRIPT_DIR=\"$(cd \"$(dirname \"$SOURCE\")\" && pwd)\"");
            w.println("APP_HOME=\"$(cd \"${SCRIPT_DIR}/..\" && pwd)\"");
            w.println();
            w.println("NATIVE_BIN=\"${APP_HOME}/bin/" + nativeImageName + "\"");
            w.println();
            w.println("# Library paths for GraalVM shared libs and ND4J/bytedeco native libraries");
            w.println("export LD_LIBRARY_PATH=\"${APP_HOME}/bin${LD_LIBRARY_PATH:+:${LD_LIBRARY_PATH}}\"");
            w.println("export DYLD_LIBRARY_PATH=\"${APP_HOME}/bin${DYLD_LIBRARY_PATH:+:${DYLD_LIBRARY_PATH}}\"");
            w.println("# Add all directories under lib/ containing platform native libraries");
            w.println("if [ -d \"${APP_HOME}/lib\" ]; then");
            w.println("    while IFS= read -r d; do");
            w.println("        LD_LIBRARY_PATH=\"${d}:${LD_LIBRARY_PATH}\"");
            w.println("        DYLD_LIBRARY_PATH=\"${d}:${DYLD_LIBRARY_PATH}\"");
            w.println("    done < <(find \"${APP_HOME}/lib\" -type f \\( -name '*.so' -o -name '*.so.*' -o -name '*.dylib' -o -name '*.dll' \\) -exec dirname {} \\; | sort -u)");
            w.println("    export LD_LIBRARY_PATH DYLD_LIBRARY_PATH");
            w.println("fi");
            w.println();
            w.println("if [ ! -x \"${NATIVE_BIN}\" ]; then");
            w.println("    echo \"error: native binary not found: ${NATIVE_BIN}\" >&2");
            w.println("    exit 1");
            w.println("fi");
            w.println();
            w.println("echo \"[" + appName + "] Starting native binary\"");
            w.println("exec \"${NATIVE_BIN}\" \\");
            w.println("    -Dspring.config.additional-location=\"optional:file:${APP_HOME}/conf/\" \\");
            w.println("    \"$@\"");
        }
        runScript.setExecutable(true);
    }

    /**
     * Ensure the data scaffolding directories exist so the assembly can pick them up.
     * These are the runtime directories that the app expects to find relative to its
     * working directory.
     */
    private void generateDataScaffolding(File projectDir) throws IOException {
        File dataDir = new File(projectDir, "data");
        String[] subdirs = {"input_documents", "shared_files", "prompt-templates", "mcp-servers", "mcp-bridges",
                "tool-definitions", "folders"};
        for (String sub : subdirs) {
            File dir = new File(dataDir, sub);
            if (!dir.exists() && !dir.mkdirs()) {
                throw new IOException("Could not create data directory: " + dir.getAbsolutePath());
            }
        }

        // Default mcp-config.json if not present
        File mcpConfig = new File(dataDir, "mcp-config.json");
        if (!mcpConfig.exists()) {
            try (PrintWriter w = new PrintWriter(new FileWriter(mcpConfig))) {
                w.println("{\"mcpServers\":{}}");
            }
        }

        // Sample placeholder files so directories aren't empty (git/assembly skip empty dirs)
        writePlaceholder(new File(dataDir, "input_documents/sample.txt"),
                "# Place your documents here for ingestion");
        writePlaceholder(new File(dataDir, "shared_files/sample-shared.txt"),
                "# Shared files accessible via MCP filesystem tool");
    }

    private void writePlaceholder(File file, String content) throws IOException {
        if (!file.exists()) {
            try (PrintWriter w = new PrintWriter(new FileWriter(file))) {
                w.println(content);
            }
        }
    }
}
